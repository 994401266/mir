	/**
	 * 新增/修改 操作吗,返回json格式数据
	 * 
	 */
	@RequestMapping("/update")
	@ResponseBody      
	public ReturnDatas saveorupdate(Model model,Comment comment,HttpServletRequest request,HttpServletResponse response) throws Exception{
		ReturnDatas returnObject = ReturnDatas.getSuccessReturnDatas();
		returnObject.setMessage(MessageUtils.UPDATE_SUCCESS);
		if (StringUtils.isBlank(SessionUser.getUserId())) {
			returnObject.setStatus(ReturnDatas.ERROR);
			returnObject.setMessage("请先登录后再评论！");
			return returnObject;
		}
		if (StringUtils.isBlank(comment.getContent())) {
			returnObject.setStatus(ReturnDatas.ERROR);
			returnObject.setMessage("评论内容不能为空！");
			return returnObject;
		}
		if (comment.getMovieId() == null) {
			returnObject.setStatus(ReturnDatas.ERROR);
			returnObject.setMessage("评论失败，缺少电影ID");
			return returnObject;
		}
		try {

			comment.setUserId(SessionUser.getUserId());
			comment.setCreated(new Date());
			commentService.save(comment);
		} catch (Exception e) {
			logger.error(e.getMessage(),e);
			e.printStackTrace();
			returnObject.setStatus(ReturnDatas.ERROR);
			returnObject.setMessage(MessageUtils.UPDATE_ERROR);
		}
		return returnObject;
	
	}
	
