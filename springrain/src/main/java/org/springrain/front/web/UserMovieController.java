	/**
	 * 新增/修改 操作吗,返回json格式数据
	 * 
	 */
	@RequestMapping("/update")
	@ResponseBody      
	public ReturnDatas saveorupdate(Model model,UserMovie userMovie,HttpServletRequest request,HttpServletResponse response) throws Exception{
		ReturnDatas returnObject = ReturnDatas.getSuccessReturnDatas();
		returnObject.setMessage(MessageUtils.UPDATE_SUCCESS);
		if (StringUtils.isBlank(SessionUser.getUserId())) {
			returnObject.setStatus(ReturnDatas.ERROR);
			returnObject.setMessage("请先登录后再评分！");
			return returnObject;
		}
		if (StringUtils.isBlank(userMovie.getScore())) {
			returnObject.setStatus(ReturnDatas.ERROR);
			returnObject.setMessage("请选择分数");
			return returnObject;
		}
		if (userMovie.getMid() == null) {
			returnObject.setStatus(ReturnDatas.ERROR);
			returnObject.setMessage("打分失败，缺少电影ID");
			return returnObject;
		}
		try {
			userMovie.setUid(SessionUser.getUserId());
			UserMovie queryBean = new UserMovie();
			queryBean.setMid(userMovie.getMid());
			queryBean.setUid(userMovie.getUid());
			List<UserMovie> list = userMovieService.findListDataByFinder(null, null,
					UserMovie.class, queryBean);
			if (CollectionUtils.isNotEmpty(list)) {
				userMovie.setId(list.get(0).getId());
			}
			userMovie.setUpdate_time(new Date());
			userMovieService.saveorupdate(userMovie);
			
		} catch (Exception e) {
			logger.error(e.getMessage(),e);
			e.printStackTrace();
			returnObject.setStatus(ReturnDatas.ERROR);
			returnObject.setMessage(MessageUtils.UPDATE_ERROR);
		}
		return returnObject;
	
	}
	
